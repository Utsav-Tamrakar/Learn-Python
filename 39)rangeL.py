#range is used to generate a sequence of numbers
r=range(5)
print(r)
l=list(r)#list constructor is used to convert the range into a list
print(l)

#range can also take two arguments, start and end
r=range(-2,2)
print(r)
l=list(r) 
print(l)  
#RANGE can also take three arguments, start, end and step (x,y,z)
# we then have a numbers below y and start from x and increment by z 
#In this way,it works
r=range(10,20,2)
print(r)
l=list(r) 
print(l)

